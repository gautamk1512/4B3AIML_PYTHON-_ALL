# Topic 05: Control Statements

# Questions
# 1. What are conditional statements in Python?
# 2. What are iterative statements?
# 3. What are unconditional statements?

# Notes
# Conditional: if, elif, else
# Iterative: for, while
# Unconditional: break, continue, pass

# Solutions
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")
for i in range(3):
    print(i)
for i in range(5):
    if i == 3:
        break
    print(i)
