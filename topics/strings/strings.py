# Topic 08: Strings

# Questions
# 1. What is a string in Python?
# 2. How do you concatenate and compare strings?
# 3. What are built-in string methods?
# 4. How do you format strings?

# Notes
# Strings are sequences of characters.
# Concatenation: + operator
# Comparison: == operator
# Methods: upper(), lower(), strip(), split(), join(), replace()
# Formatting: f-strings, format()

# Solutions
s1 = "Hello"
s2 = "World"
print(s1 + " " + s2)
print(s1 == s2)
print(s1.upper())
print(f"Welcome, {s1}!")
