# Topic 03: Input/Output

# Questions
# 1. How do you take input from the user in Python?
# 2. How do you display output in Python?
# 3. What is the difference between `input()` and `print()`?
# 4. Write an expression to add two numbers entered by the user.

# Notes
# Input: Use `input()` to get user input (always returns a string).
# Output: Use `print()` to display output.
# Expressions: Combine variables and operators, e.g., `a + b`.

# Solutions
user_value = input("Enter something: ")
print("This is output.")
# input() is used to take input from user, print() is used to display output.
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
print("Sum:", a + b)
